import turtle
def goto(x,y):
    turtle.penup()
    turtle.setheading(0)
    turtle.goto(x,y)
    turtle.pendown()

def i(x,y,z):
    turtle.penup()
    turtle.goto(x,y)
    turtle.pendown()
    if(z == 0):
        turtle.setheading(0)
    else:
        turtle.setheading(-90)
    turtle.forward(60)
    turtle.backward(60)
    turtle.right(90)
    turtle.forward(80)
    turtle.backward(160)

